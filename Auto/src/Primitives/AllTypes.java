package Primitives;

public class AllTypes {
    public static void main(String[] args) {


        //Примитивные:
        byte a = 0;
        short b = -300;
        int c = -2147483648;
        long d = 1;
        float e = 123F;
        double f = 12.1;
        char h = 'h';
        // Логический
        boolean g = false;
        //ссылочный тип данных - любой класс, в том числе и стринг.
        System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f + " " + h + " " + g);
    }
}
